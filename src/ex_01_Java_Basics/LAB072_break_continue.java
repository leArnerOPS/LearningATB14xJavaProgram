package ex_01_Java_Basics;

public class LAB072_break_continue {
    public static void main(String[]args){

        // break - exit from loop
        // use is switch and for loop
        // continue- skip and continue
        for (int i = 0; i < 50; i++) {
            if (i==5){
                continue;
            }
            System.out.println(i);
        }









    }
}
