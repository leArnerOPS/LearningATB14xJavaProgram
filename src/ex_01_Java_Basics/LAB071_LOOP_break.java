package ex_01_Java_Basics;

public class LAB071_LOOP_break {
    public static void main(String[]args) {

        for (int i = 0; i < 50; i++) { // 0 to 49 ,times ; 50
            System.out.println(i);
            if (i == 5){
                break;
            }
            System.out.println(i);
        }









    }
}
