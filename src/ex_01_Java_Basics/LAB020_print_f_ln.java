package ex_01_Java_Basics;

public class LAB020_print_f_ln {
    public static void main(String[] args) {
        int a= 20;
        int b= 30;

        System.out.println(a);
        System.out.println(b);

        System.out.print(a);
        System.out.println();
        System.out.print(b);

        System.out.println();
        System.out.printf("Value of a=%d",a);
        System.out.println();
        System.out.printf("Value of b=%d",b);



    }
}
